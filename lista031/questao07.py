'''
Fazer um algoritmo que pergunte 3 números e apresente a média aritmética entre estes 3 números.
'''

print("Abaixo, escreva três números")

n1 = float(input("Informe o primeiro número:"))
n2 = float(input("Informe o segundo número:"))
n3 = float(input("Informe o terceiro número"))

media = (n1 + n2 + n3) / 3

print("A média entre esses números é", media)